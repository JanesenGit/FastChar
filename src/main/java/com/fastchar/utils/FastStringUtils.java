    /**
     * 是否匹配
     *
     * @param pattern 可使用通配符* 例如：/druid/*
     * @param target  目标路径
     */
    public static boolean matches(String pattern, String target) {
        if (isEmpty(pattern) || isEmpty(target)) {
            return false;
        }
        String reg = pattern
                .replace("\\", "\\\\")
                .replace(".", "\\.")
                .replace("*", ".*");
        return Pattern.matches(reg, target);
    }


